/**
 * Development-only logger utility
 * Logs will be stripped in production builds
 */

const isDevelopment = process.env.NODE_ENV === 'development';

export const logger = {
  log: (...args: any[]) => {
    if (isDevelopment) {
      console.log(...args);
    }
  },
  error: (...args: any[]) => {
    // Always log errors
    console.error(...args);
  },
  warn: (...args: any[]) => {
    if (isDevelopment) {
      console.warn(...args);
    }
  },
  info: (...args: any[]) => {
    if (isDevelopment) {
      console.info(...args);
    }
  },
};

// Expose a safe global alias so legacy call sites referencing `logger` at runtime do not crash
if (typeof globalThis !== 'undefined') {
  (globalThis as any).logger = logger;
}
